def main() -> None:
    ap = argparse.ArgumentParser()

    ap.add_argument("--camera-jsonl", required=True)
    ap.add_argument(
        "--depth-yuv",
        default=None,
        help=(
            "Merged depth YUV420p10le. If omitted, use the depth_yuv "
            "field in the JSONL header."
        ),
    )
    ap.add_argument(
        "--output-prefix",
        default=None,
    )
    ap.add_argument(
        "--offset-threshold",
        required=True,
        type=float,
        help=(
            "Tool OFF threshold for "
            "avgMvPx / ((W + H) / 2.0) * 4096."
        ),
    )
    ap.add_argument(
        "--ref-combine",
        choices=["min", "mean", "max"],
        default="min",
        help=(
            "Combine past/future motion4096 values. "
            "Default=min."
        ),
    )
    ap.add_argument(
        "--min-valid-points",
        type=int,
        default=3,
    )
    ap.add_argument(
        "--corner-inset",
        type=int,
        default=0,
    )
    ap.add_argument(
        "--allow-anchor-off",
        action="store_true",
    )

    args = ap.parse_args()

    if args.offset_threshold < 0:
        raise ValueError("--offset-threshold must be nonnegative")
    if not (1 <= args.min_valid_points <= 5):
        raise ValueError("--min-valid-points must be in [1,5]")
    if args.corner_inset < 0:
        raise ValueError("--corner-inset must be nonnegative")

    camera_path = Path(args.camera_jsonl)
    header, frames = read_camera_jsonl(camera_path)

    width = int(header["width"])
    height = int(header["height"])
    pose_mode = str(header["pose_mode"])

    if args.depth_yuv:
        depth_path = Path(args.depth_yuv)
    else:
        depth_name = header.get("depth_yuv")
        if not depth_name:
            raise ValueError(
                "--depth-yuv omitted and JSONL header has no depth_yuv"
            )
        depth_path = camera_path.parent / depth_name

    if args.output_prefix:
        prefix = Path(args.output_prefix)
    else:
        prefix = camera_path.with_name(
            camera_path.stem + "_ra_bidir_sim"
        )

    grouped: Dict[int, List[CameraFrame]] = {}
    for frame in frames:
        grouped.setdefault(frame.gop_idx, []).append(frame)

    all_decisions: List[Decision] = []

    with DepthYUV420P10LE(depth_path, width, height) as depth_reader:
        for gop_idx, gop_frames in sorted(grouped.items()):
            decisions = simulate_gop(
                frames=sorted(
                    gop_frames,
                    key=lambda f: f.local_poc,
                ),
                pose_mode=pose_mode,
                depth_reader=depth_reader,
                width=width,
                height=height,
                threshold=args.offset_threshold,
                ref_combine=args.ref_combine,
                min_valid_points=args.min_valid_points,
                corner_inset=args.corner_inset,
                allow_anchor_off=args.allow_anchor_off,
            )

            all_decisions.extend(decisions)

            off_count = sum(
                not d.tool_enabled for d in decisions
            )
            print(
                f"[GOP {gop_idx}] pictures={len(decisions)}, "
                f"tool_off={off_count}, "
                f"ratio={off_count / len(decisions):.4f}"
            )

    csv_path = Path(str(prefix) + "_frames.csv")
    mask_path = Path(str(prefix) + "_mask.jsonl")
    summary_path = Path(str(prefix) + "_summary.json")

    write_csv(csv_path, all_decisions)
    write_mask_jsonl(
        mask_path,
        header,
        all_decisions,
        args,
    )

    summary = build_summary(
        header,
        all_decisions,
        args,
    )
    summary_path.parent.mkdir(parents=True, exist_ok=True)
    summary_path.write_text(
        json.dumps(summary, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    print(f"[OK] CSV     : {csv_path}")
    print(f"[OK] mask    : {mask_path}")
    print(f"[OK] summary : {summary_path}")
    print(
        f"[TOTAL] tool_off="
        f"{summary['total_tool_off_count']}/"
        f"{summary['total_picture_count']} "
        f"({summary['tool_off_ratio_all']:.4%})"
    )


if __name__ == "__main__":
    main()
